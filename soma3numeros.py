n1 = int(input("Insira o primeiro número "))
n2 = int(input("Insira o segundo número "))
n3 = int(input("Insira o terceiro número "))

if n1 == n2 == n3:
    print(f"Como os três números são iguais o resultado é {n1 + n2 + n3}")
else:
    print("Como os três números são diferentes o resultado é 0")